# Arithmetic Operators
a = 10
b = 20

# Addition Adds values on either side of the operator.
print("Addition ", (a + b))

# Subtraction Subtracts right hand operand from left hand operand.
print("Subtraction ", (a - b))

# Multiplication Multiplies values on either side of the operator Divison Divides left hand operand by right hand
# operand
print("Multiplication ", (a * b))

# Modulus Divides left hand operand by right hand operand and returns remainder Exponent Performs exponential
#  (power) calculation on operators
print("Modulus", (a % b))

# Exponent Performs exponential (power) calculation on operators
print("Exponent", a ** b)

# Floor Division - The division of operands where the result is the
# quotient in which the digits after the decimal point are removed. But if one of the operands is negative,
# the result is floored, i.e., rounded away from zero (towards negative infinity)
print("Floor division", (a // b))
